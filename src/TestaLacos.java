
public class TestaLacos {

	public static void main(String[] args) {

		for (int mult =1; mult <= 10; mult++) {
			
			for (int num = 0; num <=10; num++) {
				System.out.print(mult + " X "
			+ num + " = " + mult*num);
				System.out.println(" ");
			}
			System.out.println(" ");
		}
						
	
	}

}
